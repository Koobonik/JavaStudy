package project;

public class SimpleCalculator{
	static double plus(double d1, Double d2){
		return d1+d2;
	}
	static double minus(double d1, Double d2){
		return d1-d2;
	}
	static double multi(double d1, Double d2){
		return d1*d2;
	}
	static double div(double d1, Double d2){
		return d1/d2;
	}
	
}